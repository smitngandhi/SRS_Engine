/**
 * Generation View - Shows live SRS generation progress in main content area
 */

class GenerationView {
    constructor() {
        this.generationView = document.getElementById('generationView');
        this.formContainer = document.getElementById('formContainer');
        this.timeline = document.getElementById('generationTimeline');
        this.progressFill = document.getElementById('generationProgressFill');
        this.progressText = document.getElementById('generationProgressText');
        this.statusLabel = document.getElementById('generationStatusLabel');
        this.statusDot = document.getElementById('generationStatusDot');
        this.details = document.getElementById('generationDetails');
        
        this.currentPhase = 0;
        this.totalPhases = 4;
        this.activeItems = new Set();
        this.completedItems = new Set();
        this.phases = new Map();
    }

    show() {
        // Hide form and show generation view
        if (this.formContainer) {
            this.formContainer.style.display = 'none';
        }
        if (this.generationView) {
            this.generationView.style.display = 'block';
        }
        
        // Reset state
        this.currentPhase = 0;
        this.activeItems.clear();
        this.completedItems.clear();
        this.phases.clear();
        
        // Initialize view
        this.updateStatus('Initializing SRS generation...');
        this.updateProgress(0, 'Starting...');
        this.clearTimeline();
        this.updateDetails('🚀 Your SRS document is being generated by AI agents. This process typically takes 2-3 minutes.');
    }

    hide() {
        // Show form and hide generation view
        if (this.formContainer) {
            this.formContainer.style.display = 'block';
        }
        if (this.generationView) {
            this.generationView.style.display = 'none';
        }
    }

    updateStatus(message) {
        if (this.statusLabel) {
            this.statusLabel.textContent = message;
        }
    }

    updateProgress(percentage, text) {
        if (this.progressFill) {
            this.progressFill.style.width = `${percentage}%`;
        }
        if (this.progressText) {
            this.progressText.textContent = text;
        }
    }

    updateConnectionStatus(status) {
        if (!this.statusDot) return;
        
        this.statusDot.className = `status-dot ${status}`;
        
        const statusMessages = {
            'connecting': 'Connecting to generation service...',
            'connected': 'Connected to generation service',
            'running': 'SRS generation in progress...',
            'completed': 'SRS generation completed!',
            'error': 'Connection error'
        };
        
        if (statusMessages[status]) {
            this.updateStatus(statusMessages[status]);
        }
    }

    clearTimeline() {
        if (this.timeline) {
            this.timeline.innerHTML = '';
        }
    }

    addPhase(phase, agents) {
        const phaseElement = document.createElement('div');
        phaseElement.className = 'timeline-phase';
        phaseElement.id = `phase-${phase}`;
        
        const phaseNames = {
            1: 'Technical Requirements Analysis',
            2: 'Glossary & Assumptions',
            3: 'System Diagrams Generation',
            4: 'Document Creation'
        };
        
        const phaseDescriptions = {
            1: 'Analyzing requirements using 5 specialized AI agents',
            2: 'Creating glossary and identifying assumptions',
            3: 'Generating system architecture diagrams',
            4: 'Compiling final SRS document'
        };
        
        phaseElement.innerHTML = `
            <div class="phase-header">
                <div class="phase-title">
                    <span class="phase-icon" id="phase-icon-${phase}">${phase}</span>
                    Phase ${phase}: ${phaseNames[phase]}
                </div>
                <div class="phase-status" id="phase-status-${phase}">Waiting...</div>
            </div>
            <div class="phase-description">${phaseDescriptions[phase]}</div>
            <div class="timeline-items" id="phase-items-${phase}">
                ${agents.map(agent => this.createAgentItem(agent)).join('')}
            </div>
        `;
        
        if (this.timeline) {
            this.timeline.appendChild(phaseElement);
        }
        
        this.phases.set(phase, {
            element: phaseElement,
            agents: agents,
            completed: false
        });
    }

    createAgentItem(agentName) {
        const isTool = agentName.includes('render_mermaid') || agentName.includes('generate_srs_document');
        const icon = isTool ? '🔧' : '🤖';
        const type = isTool ? 'tool' : 'agent';
        
        const displayName = agentName.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
        
        return `
            <div class="timeline-item" id="item-${agentName}">
                <div class="item-icon ${type}" id="item-icon-${agentName}">${icon}</div>
                <div class="item-details">
                    <div class="item-name">${displayName}</div>
                    <div class="item-description">Processing...</div>
                    <div class="item-status">⏳ Starting...</div>
                </div>
            </div>
        `;
    }

    activatePhase(phase, agents) {
        this.currentPhase = phase;
        
        // Add phase to timeline
        this.addPhase(phase, agents);
        
        // Update phase status
        const phaseElement = document.getElementById(`phase-${phase}`);
        const phaseIcon = document.getElementById(`phase-icon-${phase}`);
        const phaseStatus = document.getElementById(`phase-status-${phase}`);
        
        if (phaseElement) {
            phaseElement.classList.add('active');
        }
        if (phaseIcon) {
            phaseIcon.classList.add('running');
        }
        if (phaseStatus) {
            phaseStatus.textContent = 'Running...';
        }
        
        // Update overall progress
        const progress = ((phase - 1) / this.totalPhases) * 100;
        this.updateProgress(progress, `Phase ${phase} - Running agents...`);
        this.updateConnectionStatus('running');
    }

    startAgent(agentName) {
        const item = document.getElementById(`item-${agentName}`);
        const icon = document.getElementById(`item-icon-${agentName}`);
        const status = item?.querySelector('.item-status');
        const description = item?.querySelector('.item-description');
        
        if (item) {
            item.classList.add('running');
        }
        if (icon) {
            icon.classList.add('running');
        }
        if (status) {
            status.textContent = '⏳ Running...';
        }
        if (description) {
            const displayName = agentName.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
            description.textContent = this.getAgentDescription(agentName);
        }
        
        this.activeItems.add(agentName);
        this.updateDetails(this.getCurrentAgentDescription(agentName));
    }

    completeAgent(agentName) {
        const item = document.getElementById(`item-${agentName}`);
        const icon = document.getElementById(`item-icon-${agentName}`);
        const status = item?.querySelector('.item-status');
        
        if (item) {
            item.classList.remove('running');
            item.classList.add('completed');
        }
        if (icon) {
            icon.classList.remove('running');
            icon.classList.add('completed');
        }
        if (status) {
            status.textContent = '✅ Completed';
        }
        
        this.activeItems.delete(agentName);
        this.completedItems.add(agentName);
        
        // Update phase progress
        this.updatePhaseProgress();
    }

    completePhase(phase) {
        const phaseElement = document.getElementById(`phase-${phase}`);
        const phaseIcon = document.getElementById(`phase-icon-${phase}`);
        const phaseStatus = document.getElementById(`phase-status-${phase}`);
        
        if (phaseElement) {
            phaseElement.classList.remove('active');
            phaseElement.classList.add('completed');
        }
        if (phaseIcon) {
            phaseIcon.classList.remove('running');
            phaseIcon.classList.add('completed');
        }
        if (phaseStatus) {
            phaseStatus.textContent = '✅ Completed';
        }
        
        // Update overall progress
        const progress = (phase / this.totalPhases) * 100;
        this.updateProgress(progress, `Phase ${phase} completed!`);
        
        const phaseData = this.phases.get(phase);
        if (phaseData) {
            phaseData.completed = true;
        }
    }

    updatePhaseProgress() {
        const currentPhaseData = this.phases.get(this.currentPhase);
        if (!currentPhaseData) return;
        
        const completedInPhase = currentPhaseData.agents.filter(agent => 
            this.completedItems.has(agent)
        ).length;
        const totalInPhase = currentPhaseData.agents.length;
        
        const phaseProgress = (completedInPhase / totalInPhase) * 100;
        const overallProgress = ((this.currentPhase - 1) / this.totalPhases) * 100 + 
                               (phaseProgress / this.totalPhases);
        
        this.updateProgress(overallProgress, 
            `Phase ${this.currentPhase} - ${completedInPhase}/${totalInPhase} completed`);
    }

    getAgentDescription(agentName) {
        const descriptions = {
            'introduction': 'Writing project introduction and overview',
            'overall_description': 'Creating comprehensive system description',
            'system_features': 'Detailing functional requirements',
            'external_interfaces': 'Defining system interfaces',
            'nfr': 'Specifying non-functional requirements',
            'glossary': 'Creating terminology glossary',
            'assumptions': 'Documenting system assumptions',
            'render_mermaid_png_user_interfaces': 'Generating user interface diagrams',
            'render_mermaid_png_hardware_interfaces': 'Creating hardware interface diagrams',
            'render_mermaid_png_software_interfaces': 'Designing software architecture diagrams',
            'render_mermaid_png_communication_interfaces': 'Mapping communication flow diagrams',
            'generate_srs_document': 'Compiling final SRS document'
        };
        
        return descriptions[agentName] || 'Processing...';
    }

    getCurrentAgentDescription(agentName) {
        const displayName = agentName.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
        const description = this.getAgentDescription(agentName);
        
        return `
            <div class="details-title">🔄 Currently Running</div>
            <div class="details-content">
                <strong>${displayName}</strong> is ${description.toLowerCase()}. 
                This typically takes 30-60 seconds depending on complexity.
            </div>
        `;
    }

    updateDetails(content) {
        if (this.details) {
            this.details.innerHTML = `<div class="details-content">${content}</div>`;
        }
    }

    showCompletion() {
        this.updateProgress(100, 'SRS Generation Complete!');
        this.updateConnectionStatus('completed');
        
        // Add completion message
        const completionDiv = document.createElement('div');
        completionDiv.className = 'completion-message';
        completionDiv.innerHTML = `
            <div class="completion-title">🎉 SRS Generation Complete!</div>
            <div class="completion-subtitle">Your document has been successfully generated and is ready for download.</div>
        `;
        
        if (this.generationView) {
            this.generationView.appendChild(completionDiv);
        }
        
        this.updateDetails(`
            <div class="details-title">✅ Generation Complete</div>
            <div class="details-content">
                Your SRS document has been successfully generated! 
                The document includes all technical specifications, diagrams, and requirements.
                You can now download and review your comprehensive SRS document.
            </div>
        `);
        
        // Add completion animation
        setTimeout(() => {
            if (this.generationView) {
                this.generationView.classList.add('completing');
            }
        }, 100);
    }

    // Handle trace events
    handleTraceEvent(event) {
        switch(event.event_type) {
            case 'workflow_start':
                this.activatePhase(event.data.phase, event.data.agents);
                break;
            case 'agent_start':
                this.startAgent(event.agent_name);
                break;
            case 'agent_complete':
                this.completeAgent(event.agent_name);
                break;
            case 'tool_execution':
                if (event.status === 'running') {
                    this.startAgent(event.tool_name);
                } else if (event.status === 'completed') {
                    this.completeAgent(event.tool_name);
                }
                break;
            case 'workflow_complete':
                this.completePhase(event.data.phase);
                if (event.data.phase === this.totalPhases) {
                    this.showCompletion();
                }
                break;
        }
    }
}

// Initialize generation view
let generationView = null;

document.addEventListener('DOMContentLoaded', () => {
    generationView = new GenerationView();
    
    // Export for use in other scripts
    window.generationView = generationView;
});
